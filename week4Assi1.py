# Program that counts down from 10 With while loop.
number = 10

while number > -1:
    print('CountDown with While Loops = ', number)
    number = number - 1

# Program that counts down from 10 With for loop.
for i in range(10, 0, -1):
    print('CountDown with For Loops = ', i)

# Write a program that prints 2n where n goes from 1 to 100
for n in range(1, 101):
    print('The number is:', 2**n)





